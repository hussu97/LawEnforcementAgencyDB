    private void jButton1generateClicked(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1generateClicked
        // TODO add your handling code here:
        IDField.setText("");
        NameField.setText("");
        SSNField.setText("");
        ContactField.setText("");
        HireField.setText("");
        EmployeeTypeField.setText("");
        SalaryField.setText("");
        if(EmployeeField.getText().trim().equals("")){
        }
        else{
            Connection conn=OracleJDBCConnection.connectDataBase();
            try {
                Statement st=conn.createStatement();
                String sql="SELECT INMATE_ID,INMATE_NAME,IMPRISON_DATE,IMPRISON_DATE+SENTENCE_PERIOD,PRISON_LOCATION,INMATE.CELL_ID,CELL_TYPE FROM INMATE,PRISON,CELL "
                + "WHERE INMATE.PRISON_ID=PRISON.PRISON_ID AND INMATE.CELL_ID=CELL.CELL_ID AND ";
                if(EmployeeField.getText().trim().startsWith("1")||
                    EmployeeField.getText().trim().startsWith("2")||
                    EmployeeField.getText().trim().startsWith("3")||
                    EmployeeField.getText().trim().startsWith("4")||
                    EmployeeField.getText().trim().startsWith("5")||
                    EmployeeField.getText().trim().startsWith("6")||
                    EmployeeField.getText().trim().startsWith("7")||
                    EmployeeField.getText().trim().startsWith("8")||
                    EmployeeField.getText().trim().startsWith("9")){
                    sql+="INMATE_ID= "+EmployeeField.getText().trim();
                }
                else
                sql+="LOWER(INMATE_NAME) LIKE LOWER('%"+EmployeeField.getText().trim()+"%')";
                ResultSet rs=st.executeQuery(sql);
                while(rs.next()){
                    IDField.setText(rs.getString(1));
                    NameField.setText(rs.getString(2));
                    SSNField.setText(rs.getString(3).replaceFirst(" 00:00:00.0", ""));
                    ContactField.setText(rs.getString(4).replaceFirst(" 00:00:00.0", ""));
                    HireField.setText(rs.getString(5));
                    EmployeeTypeField.setText(rs.getString(6));
                    SalaryField.setText(rs.getString(7));
                }
                if(IDField.getText().trim().equals(""))
                JOptionPane.showMessageDialog(null,"Inmate not found");
            } catch (SQLException ex) {
                Logger.getLogger(AddEmployee1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1generateClicked

